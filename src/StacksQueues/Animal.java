package StacksQueues;

public class Animal {
int order;
String name;

public Animal(String name) {
	this.name = name;
}

public int getOrder() {
	return order;
}

public void setOrder(int order) {
	this.order = order;
}

public String getName() {
	return name;
}

public void setName(String name) {
	this.name = name;
}

}
